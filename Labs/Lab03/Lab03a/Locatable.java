/**
 * This interface provides a set of methods, namely, getX(), getX(), setLocation(int x, int y).
 * @author Osama Tanveer
 * @version 1.0, 12 March 2019
 */
public interface Locatable {
  
  // to get the x-coordinate of the shape 
  int getX();
  
  // to get the y-coordinate of the shape
  int getY();
  
  // to set location of the shape 
  void setLocation(int x, int y);
}